import { Utils } from './utils';

describe('Utils', () => {
  it('compressStringIfTooLong function should return compressed string if input is too long', () => {
    expect(Utils.compressStringIfTooLong('012345678901234567890123456789012345678901234567890123456789012345678'))
      .toEqual('012345678901234567890123456789012345678901234567890123456789012345678');
    expect(Utils.compressStringIfTooLong('0123456789012345678901234567890123456789012345678901234567890123456789'))
      .toEqual('0123456789012345678901234567890123456789012345678901234567890123456789');
    expect(Utils.compressStringIfTooLong('012345678901234567890123456789012345678901234567890123456789012345x7890'))
      .toEqual('012345678901234567890123456789012345678901234567890123456789012345...7890');
    expect(Utils.compressStringIfTooLong('012345678901234567890123456789012345678901234567890123456789012345yy8901'))
      .toEqual('012345678901234567890123456789012345678901234567890123456789012345...8901');
  });
});
